class Solution {
public:
    vector<int> res;
    vector<int> preorder(Node* root) {
        
        if(root==NULL)
            return res;
        
        preorderHelper(root);
        return res;
        
    }
    
    void preorderHelper(Node* root)
    {
        if(root==NULL)
            return;
        
        res.push_back(root->val);
        for(auto child: root->children)
        {
            preorder(child);
        }
        
        return;
    }
};

//iterative

class Solution {
public:
    vector<int> preorder(Node* root) {
        vector<int> result;
        if (root == nullptr) {
            return result;
        }
        
        stack<Node*> stk;
        stk.push(root);
        while (!stk.empty()) {
            Node* cur = stk.top();
            stk.pop();
            result.push_back(cur -> val);
            for (int i = cur -> children.size() - 1; i >= 0; i--) {
                if (cur -> children[i] != nullptr) {
                    stk.push(cur -> children[i]);
                }
            }
        }
        return result;
    }
};